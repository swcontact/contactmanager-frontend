export class Contact {
    public id: number;
    public firstName: string;
    public lastName: string;
    public category: string;
    public contact: string;
    public email: string;
    public birthday: string;
    public telephone: string;
    constructor() {
        this.id = 0;
        this.firstName = "";
        this.lastName = "";
        this.category = "Customer";
        this.contact = "";
        this.email = "";
        this.birthday = "";
        this.telephone = "";
    }
}
